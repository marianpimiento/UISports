function pon_prefijo(codigo,idevento) {
	parent.document.form_busqueda.codigousuario.value=codigousuario;
	parent.document.form_busqueda.idevento.value=idevento;
}

function limpiar() {
	parent.document.form_busqueda.codigousuario.value="";
	parent.document.form_busqueda.idevento.value="";
	parent.document.form_busqueda.idinscripcion.value="";
}